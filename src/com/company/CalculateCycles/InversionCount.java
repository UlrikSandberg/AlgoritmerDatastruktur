package com.company.CalculateCycles;

public class InversionCount {

    private int InversionCount;
    private int[] Input;

    public int getInversionCount() {
        return InversionCount;
    }

    public int[] getInput() {
        return Input;
    }

    public InversionCount(int inversionCount, int[] input) {
        InversionCount = inversionCount;
        Input = input;
    }

}
