package com.company.Sorting.Quicksort;

public class PartitionMark {

    private int q = 0;
    private int k = 0;

    public PartitionMark(int q, int k) {
        this.q = q;
        this.k = k;
    }

    public int getQ() {
        return q;
    }

    public int getK() {
        return k;
    }
}
